// Loops --> It is used to print or repeat code multiple times 

//1. Conditional Loops 

// For Loop
for(var i =0 ; i < 5; i++) {
    console.log(i, "Bharath")
} 

// while loop 
var j = 0
while(j < 10) {
    console.log(j)
    j++
}


/// do while loop : Runs atleast once even if the condition is failed
var k = 15
do {
    console.log("Ayyappa")
    k++;
} while (k < 1);

